package com.shop_final.project.model;

public class EventVO {
	private String eventNo;
	private String eprdName;
	private int eprdDiscount;
	private int eprdPrice;
	private String eprdImg;
	private String eventCtgId;
	private String prdNo;
	
	public String getEventNo() {
		return eventNo;
	}
	public void setEventNo(String eventNo) {
		this.eventNo = eventNo;
	}
	public String getEprdName() {
		return eprdName;
	}
	public void setEprdName(String eprdName) {
		this.eprdName = eprdName;
	}
	public int getEprdDiscount() {
		return eprdDiscount;
	}
	public void setEprdDiscount(int eprdDiscount) {
		this.eprdDiscount = eprdDiscount;
	}
	public int getEprdPrice() {
		return eprdPrice;
	}
	public void setEprdPrice(int eprdPrice) {
		this.eprdPrice = eprdPrice;
	}
	public String getEprdImg() {
		return eprdImg;
	}
	public void setEprdImg(String eprdImg) {
		this.eprdImg = eprdImg;
	}
	public String getEventCtgId() {
		return eventCtgId;
	}
	public void setEventCtgId(String eventCtgId) {
		this.eventCtgId = eventCtgId;
	}
	public String getPrdNo() {
		return prdNo;
	}
	public void setPrdNo(String prdNo) {
		this.prdNo = prdNo;
	}

}
